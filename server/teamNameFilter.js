// ============================================================
// teamNameFilter.js — валидация и фильтрация названий команд.
//   • длина 2–20 символов;
//   • только буквы (рус/лат), цифры и ОДИНОЧНЫЕ пробелы (без спецсимволов,
//     без ведущих/концевых/двойных пробелов);
//   • базовый мат-фильтр (RU + EN) по нормализованной форме (анти-обход:
//     схлопывание пробелов и простой leet-маппинг).
// validateTeamName(raw) → { ok: true, name } | { error: '<причина>' }
// ============================================================

const MIN = 2, MAX = 20;

// Разрешённые символы: буквы рус/лат, цифры; слова разделяются одиночным пробелом.
const SHAPE = /^[A-Za-zА-Яа-яЁё0-9]+( [A-Za-zА-Яа-яЁё0-9]+)*$/;

// Корни нецензурной лексики (подстрочное совпадение по нормализованной форме).
// Список намеренно компактный — базовый фильтр для учебной среды.
const BAD_STEMS = [
  // RU
  'хуй', 'хуе', 'хуё', 'хуи', 'пизд', 'еба', 'ебл', 'ебу', 'ёба', 'ебан', 'ебат',
  'бляд', 'блят', 'бля', 'сука', 'суки', 'залуп', 'мудак', 'мудил', 'гондон',
  'говно', 'гавно', 'дерьм', 'пидор', 'пидар', 'педик', 'долбоёб', 'долбоеб',
  'ублюд', 'шлюх', 'мраз', 'выеб', 'уеб', 'въеб', 'наеб', 'отъеб', 'подъеб',
  'манда', 'елда', 'дрочь', 'дроч', 'сперм', 'порн', 'сучка', 'сучку',
  // EN
  'fuck', 'shit', 'bitch', 'cunt', 'asshole', 'dick', 'pussy', 'bastard',
  'nigger', 'nigga', 'faggot', 'whore', 'slut', 'cock', 'wank', 'twat', 'prick',
];

// Простой leet-маппинг + схлопывание, чтобы «п0рн0», «s h i t» и т.п. ловились.
const LEET = { '0': 'о', '3': 'е', '4': 'ч', '1': 'и', '@': 'а', '$': 's', '5': 's', '7': 't', '8': 'в' };

function normalizeForProfanity(s) {
  return s
    .toLowerCase()
    .replace(/ё/g, 'е')
    .replace(/[0-9@$]/g, ch => LEET[ch] ?? ch)
    .replace(/[^a-zа-я]/g, '');   // убираем всё, кроме букв (в т.ч. пробелы)
}

function containsProfanity(name) {
  const norm = normalizeForProfanity(name);
  return BAD_STEMS.some(stem => norm.includes(stem));
}

function validateTeamName(raw) {
  const name = (raw || '').toString().trim();
  if (!name) return { error: 'Введите название команды' };
  if (name.length < MIN) return { error: `Название слишком короткое (минимум ${MIN} символа)` };
  if (name.length > MAX) return { error: `Название слишком длинное (максимум ${MAX} символов)` };
  if (/\s{2,}/.test(name)) return { error: 'Уберите двойные пробелы в названии' };
  if (!SHAPE.test(name)) {
    return { error: 'Только буквы, цифры и одиночные пробелы (без спецсимволов)' };
  }
  if (containsProfanity(name)) {
    return { error: 'Название содержит недопустимые слова' };
  }
  return { ok: true, name };
}

module.exports = { validateTeamName, containsProfanity, MIN, MAX };
